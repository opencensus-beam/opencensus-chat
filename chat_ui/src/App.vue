    <v-navigation-drawer temporary v-model="drawerToggle">
      <v-list>
        <v-list-tile>
          <v-list-tile-action>
            <v-icon>supervisor_account</v-icon>
          </v-list-tile-action>
          <v-list-tile-content>
            Registered Users {{users.length}}
          </v-list-tile-content>
        </v-list-tile>
      </v-list>
      <v-list >
        <v-list-tile avatar v-for="user in users" @click="">
          <v-list-tile-avatar>
            <v-icon left>face</v-icon>
          </v-list-tile-avatar>
          <v-list-tile-content>
            <v-list-tile-title>{{user.name}}</v-list-tile-title>
          </v-list-tile-content>
        </v-list-tile>
      </v-list>
    </v-navigation-drawer>
