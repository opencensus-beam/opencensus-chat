      <router-view></router-view>
    </main>
  </v-app>
</template>

<script>
  export default {
    data () {
      return {
        drawerToggle: false
      }
    },
    computed: {
      menuItems () {
        let items = [
          { icon: 'face', title: 'Register', route: '/register' },
          { icon: 'lock_open', title: 'Login', route: '/login' }
        ]
        if (this.userIsAuthenticated) {
          items = [
            // {icon: 'chat', title: 'Create a Chat', route: '/create'}
          ]
        }
        return items
      },
      userIsAuthenticated () {
        return this.$store.getters.currentUser !== null && this.$store.getters.currentUser !== undefined
      },
      users () {
        return this.$store.getters.users
      }
    }
  }
</script>

